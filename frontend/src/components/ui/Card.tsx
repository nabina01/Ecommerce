import React from 'react';
import { cn } from '../../lib/utils';

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'hover' | 'bordered';
  padding?: 'none' | 'sm' | 'md' | 'lg';
}

export const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', padding = 'md', children, ...props }, ref) => {
    const variants = {
      default: 'rounded-2xl bg-white shadow-soft dark:bg-secondary-900',
      hover: 'rounded-2xl bg-white shadow-soft dark:bg-secondary-900 transition-all duration-300 hover:shadow-soft-lg hover:-translate-y-1',
      bordered: 'rounded-2xl border border-secondary-200 bg-white dark:border-secondary-700 dark:bg-secondary-900',
    };

    const paddings = {
      none: '',
      sm: 'p-4',
      md: 'p-6',
      lg: 'p-8',
    };

    return (
      <div
        ref={ref}
        className={cn(variants[variant], paddings[padding], className)}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

